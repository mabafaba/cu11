	<div class="unit w-1-3 regular longtext">
	The Karl-Marx-Allee in Berlin is a wide street linked to Alexanderplatz. The strip between Alexanderplatz and Strausberger Platz varies between six and nine lanes. Visualising google traffic predictions, we find that except for peak hours on weekends, a single lane in each direction would be sufficient for the current amount of car traffic.  Including some of the now practically inaccessible spaces in the center of the street, that is a 19000 sqm strip of empty space, only 200m away from Alexanderplatz.</div>
	<div class="unit w-1-3 regular longtext">
	We propose a minimally invasive, tactical intervention to close one lane per side permanently and another lane per side at all times except during the morning and evening traffic peak. The permanently closed lane is just wide enough to be filled with market stalls to serve people on the temporarily closed lanes. <br>
	It seems probable that the size and unique location of the available market area could draw in both, tourists from Alexanderplatz as well as the many residents of the residential buildings bordering Karl-Marx-Allee. 
    </div>
    <div class="unit w-1-3 regular longtext">
	
	With the dynamics of the classic <a href="http://epn.sagepub.com/content/10/4/371.full.pdf+html">Wilson &amp; Harris spatial interaction retail model</a> in mind, a tactical implementation can not be made by starting with a small number of stalls; they could not create enough attraction. Instead, the full space should be filled at once, but starting with the most simple forms of a market, and slowly becoming more professional and less temporary if the concept proves successful: a flea market, then food carts and market stalls. Those, if successfull over time, can be slowly advanced to wooden booths, containers and eventually some regular low rise buildings.
